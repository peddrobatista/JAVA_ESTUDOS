package com.company;

public class OperadoresIncrementaisDecrementais {
    public static void main(String[] args){
        int x = 4;
        System.out.println(x++); //X +1 = 5
        System.out.println(++x); //x +1 = 6
        System.out.println(x--); //X -1 = 5
        System.out.println(--x); //x -1 = 4
    }
}
