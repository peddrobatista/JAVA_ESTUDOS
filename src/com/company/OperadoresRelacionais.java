package com.company;

public class OperadoresRelacionais {
    public static void main(String[] args){
        int a = 20, b = 30, c = 1, d = 20;
        System.out.println(" Operadores relacionais ");
        System.out.println(b < a); //Menor que
        System.out.println(a == b); //Igual a
        System.out.println(c > b); //Maior que
        System.out.println(a != d); //Diferente de
        System.out.println(a <= d); // Menor ou igual a
        System.out.println(c >= b); // Maior ou igual a

    }
}
